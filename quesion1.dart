// Q.1 Write a Dart program to check if a given number is positive, negative, or zero.

void main() { 
  
 double number = 0;
  
  if (number > 0){
    print("Your number is positive: $number");
  }
  else if(number < 0){
    print("Your number is negative: $number");
  }
  else {
    print("Your number is zero: $number");
  }  
}


